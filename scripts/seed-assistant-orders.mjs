async function run() {
  if (!process.env.DATABASE_URL) { console.log("[seed-orders] No DATABASE_URL — skipping."); return; }
  await client.connect();
  console.log("[seed-orders] Seeding orders assistant entries...");
  let inserted = 0;
  for (const e of entries) {
    const id = crypto.randomUUID();
    try {
      await client.query(
        `INSERT INTO "AssistantEntry" (id, page, question, answer, tags, "isDefault")
         VALUES ($1,$2,$3,$4,$5,$6)
         ON CONFLICT DO NOTHING`,
        [id, e.page, e.question, e.answer, e.tags, e.isDefault]
      );
      inserted++;
    } catch (err) { console.warn("[seed-orders] warn:", err.message); }
  }
  await client.end();
  console.log(`[seed-orders] Done — ${inserted} entries.`);
}

run().catch(e => { console.error("[seed-orders] Fatal:", e.message); process.exit(1); });
